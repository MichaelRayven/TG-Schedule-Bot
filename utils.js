export const getWeekDayName = (day) => {
    switch (day) {
        case 0:
            return "Понедельник"
        case 1:
            return "Вторник"
        case 2:
            return "Среду"
        case 3:
            return "Четверг"
        case 4:
            return "Пятницу"
        case 5:
            return "Субботу"
        case 6:
            return "Воскресение"
    }
}